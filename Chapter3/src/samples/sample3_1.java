package samples;

/**
 * 算術演算子
 *
 * ※packageとは、クラスをフォルダ別に保存するための階層
 *
 * @author AKAKE
 *
 */
public class sample3_1 {

	public static void main(String[] args) {

		System.out.println(10 + 3);
		System.out.println(10 - 3);
		System.out.println(10 * 3);
		System.out.println(10 / 3);
		System.out.println(10 / 3.0);
		System.out.println(10 % 3); //剰余算
	}

}
