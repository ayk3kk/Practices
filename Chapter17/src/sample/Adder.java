package sample;

public class Adder {

	private int number;

	public Adder(int number) {
		super();
		this.number = number;
	}

	public void add() {

		this.number++;
	}

	public int getNumber() {
		return this.number;
	}

	public void setNumber(int number) {
		this.number = number;
	}


}
